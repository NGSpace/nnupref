package io.github.ngspace.nnupref;

public class MapWrapperMethodException extends RuntimeException {
	
	private static final long serialVersionUID = -8632764025580817681L;
	
	public MapWrapperMethodException() {}
	public MapWrapperMethodException(String message) {super(message);}
	public MapWrapperMethodException(Throwable cause) {super(cause);}
	public MapWrapperMethodException(String message, Throwable cause) {super(message, cause);}
	public MapWrapperMethodException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {super(message, cause, enableSuppression, writableStackTrace);}
}
